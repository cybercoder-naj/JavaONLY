
public class FirstCode {
	public static void main(String args[]) {
	
	long l = 500000l; //8 bytes
	double d = 5.5; // decimal points
	float f = 5.5f; //decimal points
	int i = 5000; // 4 bytes
	short s = 500; // 2 bytes
	byte b = 5; // 1 byte
	char c = 'a'; 
	
	double d1 = 5; //implicit
	int k = (int)d1;
	System.out.println(k);
	System.out.println("Hello World");
	}
}
